namespace Addin
{
    /// <summary>
    ///     Represents a single log message
    /// </summary>
    public class Log
    {
        /// <summary>
        ///     Label id
        /// </summary>
        public string labelId { set; get; }

        /// <summary>
        ///     Label text
        /// </summary>
        public string label { set; get; }

        /// <summary>
        ///     Label file id
        /// </summary>
        public string labelFile { set; get; }
    }
}